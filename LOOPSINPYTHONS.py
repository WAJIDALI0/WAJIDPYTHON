# how to we can define for loop in python
# simple for loop
for i in range(5):
    print(i)
#      how to we define while loop in python
i = 2
while (i <= 6):
    print(i)
    i = i + 1
# for loop increasing range
for i in range(2, 5, +2):
    print(i)
#     for loop decresing range
for i in range(10, 0, -3):
    print(i)
